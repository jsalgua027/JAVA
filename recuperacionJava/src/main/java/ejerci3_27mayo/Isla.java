public class Isla {
    
    private int fila;
    private int columna;
  
    public Isla() {
    }

    public int getFila() {
        return fila;
    }

    public void setFila(int fila) {
        this.fila = fila;
    }

    public int getColumna() {
        return columna;
    }

    public void setColumna(int columna) {
        this.columna = columna;
    }

    @Override
    public String toString() {
        return "Isla{" + "fila=" + fila + ", columna=" + columna + '}';
    }

    
    
  
    }


    
    
    
    

